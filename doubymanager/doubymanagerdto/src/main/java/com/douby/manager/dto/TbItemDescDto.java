public class TbItemDescDto implements Serializable{
    private static final long serialVersionUID = 1473427006798219179L;
    private Long itemId;

    private Date created;

    private Date updated;

    private String itemDesc;

    public Long getItemId() {
        return itemId;
    }

    public void setItemId(Long itemId) {
        this.itemId = itemId;
    }

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }

    public Date getUpdated() {
        return updated;
    }

    public void setUpdated(Date updated) {
        this.updated = updated;
    }

    public String getItemDesc() {
        return itemDesc;
    }

    public void setItemDesc(String itemDesc) {
        this.itemDesc = itemDesc == null ? null : itemDesc.trim();
    }

    public TbItemDescDto() {
    }

    public TbItemDescDto(TbItemDesc tbItemDesc) {
        this.itemId = tbItemDesc.getItemId();
        this.created = tbItemDesc.getCreated();
        this.updated = tbItemDesc.getUpdated();
        this.itemDesc = tbItemDesc.getItemDesc();
    }
}
