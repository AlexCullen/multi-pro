public class TbContentCategoryDto implements Serializable{

    private static final long serialVersionUID = -1121892094685681390L;
    private Long id;

    private Long parentId;

    private String name;

    private Integer status;

    private Integer sortOrder;

    private Boolean isParent;

    private Date created;

    private Date updated;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getParentId() {
        return parentId;
    }

    public void setParentId(Long parentId) {
        this.parentId = parentId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Integer getSortOrder() {
        return sortOrder;
    }

    public void setSortOrder(Integer sortOrder) {
        this.sortOrder = sortOrder;
    }

    public Boolean getParent() {
        return isParent;
    }

    public void setParent(Boolean parent) {
        isParent = parent;
    }

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }

    public Date getUpdated() {
        return updated;
    }

    public void setUpdated(Date updated) {
        this.updated = updated;
    }

    public TbContentCategoryDto() {
    }

    public TbContentCategoryDto(TbContentCategory tbContentCategory) {
        this.id = tbContentCategory.getId();
        this.parentId = tbContentCategory.getParentId();
        this.name = tbContentCategory.getName();
        this.status = tbContentCategory.getStatus();
        this.sortOrder = tbContentCategory.getSortOrder();
        this.isParent = tbContentCategory.getIsParent();
        this.created = tbContentCategory.getCreated();
        this.updated = tbContentCategory.getUpdated();
    }
}
